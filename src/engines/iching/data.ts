// ── 64괘 조회 테이블 ─────────────────────────────────────
// HEXAGRAM_MAP[상괘 인덱스][하괘 인덱스] = HEXAGRAMS 배열 인덱스

function buildHexagramMap(): number[][] {
  const map: number[][] = Array.from({ length: 8 }, () => Array(8).fill(-1));
  for (let i = 0; i < HEXAGRAMS.length; i++) {
    map[HEXAGRAMS[i].upper][HEXAGRAMS[i].lower] = i;
  }
  return map;
}

export const HEXAGRAM_MAP: number[][] = buildHexagramMap();

// ── 괘 조회 함수 ────────────────────────────────────────

export function getHexagram(upper: number, lower: number): Hexagram {
  const idx = HEXAGRAM_MAP[upper][lower];
  if (idx < 0) throw new Error(`Invalid trigram combination: upper=${upper}, lower=${lower}`);
  return HEXAGRAMS[idx];
}

